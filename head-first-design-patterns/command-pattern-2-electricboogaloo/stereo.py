class Stereo:


    def on(self):
        print("Stereo powered on")


    def off(self):
        print("Stereo powered off")


    def set_cd(self):
        print("set to cd")


    def set_dvd(self):
        print("set to dvd")


    def set_radio(self):
        print("set to radio")


    def set_volume(self, volume):
        print(f"volume set to {volume}")
